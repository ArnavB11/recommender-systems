import numpy as np
import math

def calculate_list_psnr(original_scores, variant_scores, max_val=5.0):
    """
    Calculates the Peak Signal-to-Noise Ratio (PSNR) between two recommendation lists
    based on their item scores (e.g., NCF predicted ratings).
    """
    original_scores = np.array(original_scores)
    variant_scores = np.array(variant_scores)
    
    # Check if lists are the same length
    if len(original_scores) != len(variant_scores):
        raise ValueError("Original and variant lists must have the same length for PSNR computation.")
        
    # Calculate Mean Squared Error (MSE) between the two ordered score vectors
    mse = np.mean((original_scores - variant_scores) ** 2)
    
    if mse == 0:
        return float('inf')  # The lists have identical scores (no noise)
        
    psnr = 10 * math.log10((max_val ** 2) / mse)
    return psnr

def calculate_dynamic_thresholds(psnr_values, lower_percentile=25, upper_percentile=75):
    """
    Dynamically calculates the lower and upper PSNR thresholds based on the 
    statistical distribution of the generated lists for a specific user.
    """
    # Filter out mathematically identical lists (infinite PSNR) so they don't break the statistics
    finite_psnrs = [p for p in psnr_values if not math.isinf(p)]
    
    if not finite_psnrs:
        return 0.0, float('inf')
        
    # Find the statistical boundaries based on percentiles
    lower_thresh = np.percentile(finite_psnrs, lower_percentile)
    upper_thresh = np.percentile(finite_psnrs, upper_percentile)
    
    return lower_thresh, upper_thresh

def filter_by_psnr_sweetspot(original_list, variant_lists, movie_scores_dict, 
                             lower_percentile=25, upper_percentile=75, max_val=5.0):
    """
    Dynamically filters variant lists for a user by calculating the mathematical sweet spot.
    
    Args:
        original_list (list): The original highly-accurate NCF top N movies for a user.
        variant_lists (list of lists): The multiple variant lists generated by weighted roulette.
        movie_scores_dict (dict): Mapping from movie ID to its predicted rating/score for this user.
                                  (e.g. { 'm1': 4.5, 'm2': 3.2 ... })
        lower_percentile (int): Lower statistical boundary of the distribution (default: 25th percentile).
        upper_percentile (int): Upper statistical boundary of the distribution (default: 75th percentile).
        max_val (float): Maximum possible rating scale value.
        
    Returns:
        tuple: (accepted_variants, calculated_lower_threshold, calculated_upper_threshold)
               accepted_variants is a list of dicts containing the list and its PSNR.
    """
    original_scores = [movie_scores_dict.get(m, 0.0) for m in original_list]
    
    # 1. Calculate PSNR for every generated variant
    variant_data = []
    psnr_values = []
    
    for variant in variant_lists:
        variant_scores = [movie_scores_dict.get(m, 0.0) for m in variant]
        psnr_val = calculate_list_psnr(original_scores, variant_scores, max_val=max_val)
        
        variant_data.append({
            'list': variant,
            'psnr': psnr_val
        })
        psnr_values.append(psnr_val)
        
    # 2. Mathematically determine the threshold bounds specific to this user's generated variants
    lower_thresh, upper_thresh = calculate_dynamic_thresholds(
        psnr_values, lower_percentile, upper_percentile
    )
    
    # 3. Filter using these dynamically calculated thresholds
    accepted_variants = []
    for data in variant_data:
        # Check if it falls in the calculated mathematical sweet spot!
        if lower_thresh <= data['psnr'] <= upper_thresh:
            accepted_variants.append(data)
            
    return accepted_variants, lower_thresh, upper_thresh

